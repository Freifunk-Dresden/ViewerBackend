package de.freifunkdresden.viewerbackend;

import com.google.gson.JsonObject;
import java.util.Arrays;
import java.util.List;

public class Location {

    //TODO: Load from json file
    private static final List<Location> INVALID_LOCATIONS = Arrays.asList(
            new Location(51.05405, 13.74364),
            new Location(51.054741, 13.742642),
            new Location(51.033865, 13.653252),
            new Location(51.05448, 13.74362)
    );

    private final double latitude;
    private final double longitude;

    public Location(double latitude, double longitude) {
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public boolean isValid() {
        return !(INVALID_LOCATIONS.contains(this) || Double.isNaN(latitude) || Double.isNaN(longitude));
    }

    public JsonObject toJson() {
        JsonObject location = new JsonObject();
        location.addProperty("latitude", latitude);
        location.addProperty("longitude", longitude);
        return location;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + (int) (Double.doubleToLongBits(this.latitude) ^ (Double.doubleToLongBits(this.latitude) >>> 32));
        hash = 97 * hash + (int) (Double.doubleToLongBits(this.longitude) ^ (Double.doubleToLongBits(this.longitude) >>> 32));
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Location other = (Location) obj;
        if (Double.doubleToLongBits(this.latitude) != Double.doubleToLongBits(other.latitude)) {
            return false;
        }
        if (Double.doubleToLongBits(this.longitude) != Double.doubleToLongBits(other.longitude)) {
            return false;
        }
        return true;
    }

}
